package AssignWeek3;

public class Assignment5_ForLoops {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i += 2) {
            System.out.println();
        }
        System.out.println();
        for (int i = 1; i <= 10; i *= 2) {
            System.out.println();

        }
    }
}